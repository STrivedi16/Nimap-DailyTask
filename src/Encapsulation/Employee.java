package Encapsulation;

public class Employee 
{
		private String name ;
		private String email;
		private String Password;
		private int salalry;
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getEmail() {
			return email;
		}
		public void setEmail(String email) {
			this.email = email;
		}
		public String getPassword() {
			return Password;
		}
		public void setPassword(String password) {
			Password = password;
		}
		public int getSalalry() {
			return salalry;
		}
		public void setSalalry(int salalry) {
			this.salalry = salalry;
		}
		
		
}



// Encapsulation - It is a Mechanisum of wraping data and code Actiong on data together on single unit .
// Encapsulation means data Hiding 
// Here data is set by Setter method  and Get by Getter Method 

